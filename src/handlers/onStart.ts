import type { Context } from 'grammy';

export async function onStart(ctx: Context): Promise<void> {
  await ctx.reply(
    'Привет! Я умею две вещи.\n\n' +
      '📝 <b>Пересказ.</b> Просто пришли ссылку на статью или YouTube-видео — ' +
      'верну краткий пересказ на русском прямо в чат.\n\n' +
      '📄 <b>Статья.</b> <code>/article &lt;ссылка&gt;</code> — соберу из видео полноценную ' +
      'статью с заголовками и пришлю .md-файлом. Ничего не сокращаю: убираю только рекламу ' +
      'и мусор устной речи. Длинное видео обрабатывается несколько минут.\n' +
      'Для обычной ссылки (не видео) просто отдам текст страницы в .md.',
    { parse_mode: 'HTML' },
  );
}
